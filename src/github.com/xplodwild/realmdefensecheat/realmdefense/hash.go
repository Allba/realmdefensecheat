var kSecret = []byte{0x29, 0x4e, 0x7a, 0x78, 0x9b, 0x8a, 0xe7, 0xc3, 0xa4, 0xce, 0x24, 0xa8, 0x6b, 0x80, 0x5c, 0x57}

// ComputeHash computes the HTTP Header "Hash". Hello, Crypto.ComputeHash ;-)
func ComputeHash(body []byte) string {
	h := md5.New()
	h.Write(body)
	h.Write(kSecret)
	return fmt.Sprintf("%x", h.Sum(nil))
}
